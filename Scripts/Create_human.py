def prepare_vertex_coord(data_str):
    x = float(data_str.split(" ")[1])
    y = float(data_str.split(" ")[2])
    z = float(data_str.split(" ")[3])
    return [x, y, z]


def prepare_face_index(data_str):
    data_str = data_str[2:]
    vertex_index = []
    data_block = data_str.split()
    for d in data_block:
        vertex_index.append(int(d.split('/')[0]) - 1)
    return vertex_index


def prepare_lines_index(data_str):
    data_str = data_str[2:]
    vertex_index = []
    data_block = data_str.split()
    for d in data_block:
        vertex_index.append(int(d.split('/')[0]) - 1)
    vertex_index.append(vertex_index[0])
    return vertex_index


